/* Se declaren dos variables dinámicas de pilas de tipo punto flotante de doble precisión. 
Ambas variables deben inicializarse con números distintos de cero. */

#include <stdio.h>

int main(void) {
    double variable_1 = 5.4, variable_2 = 9.3;

    printf("El valor de variable_1 es: %.2lf\n", variable_1);
    printf("El valor de variable_2 es: %.2lf\n", variable_2);

    return 0;
}